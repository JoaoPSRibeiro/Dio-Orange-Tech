package edu.joao.Operadores;

public class OperadoresUnarioNegtivo {
    public static void main(String[] args) {
        int numero = 5; // valor inicial demonstra número positivo por padrão
        numero = - numero;  // aqui estamos negativando a variável numero

            System.out.println(numero);

        numero = numero * -1; // para converter o numero negativo em positivo, devemos multiplicar por -1 e assim inverte o sinal 

            System.out.println(numero);

    }
    
}
